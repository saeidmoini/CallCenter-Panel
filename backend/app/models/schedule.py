from datetime import datetime, time
from sqlalchemy import Integer, Boolean, DateTime, Time, ForeignKey, func
from sqlalchemy.orm import Mapped, mapped_column, relationship

from ..core.db import Base


class ScheduleConfig(Base):
    __tablename__ = "schedule_configs"

    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
    company_id: Mapped[int | None] = mapped_column(ForeignKey("companies.id"), unique=True, nullable=True)
    skip_holidays: Mapped[bool] = mapped_column(Boolean, default=True)
    enabled: Mapped[bool] = mapped_column(Boolean, default=True)
    disabled_by_dialer: Mapped[bool] = mapped_column(Boolean, default=False)
    wallet_balance: Mapped[int] = mapped_column(Integer, default=0)
    cost_per_connected: Mapped[int] = mapped_column(Integer, default=0)
    version: Mapped[int] = mapped_column(Integer, default=1)
    updated_at: Mapped[datetime] = mapped_column(DateTime(timezone=True), server_default=func.now(), onupdate=func.now())

    # Relationships
    company = relationship("Company")


class ScheduleWindow(Base):
    __tablename__ = "schedule_windows"

    id: Mapped[int] = mapped_column(Integer, primary_key=True)
    company_id: Mapped[int | None] = mapped_column(ForeignKey("companies.id"), index=True, nullable=True)
    day_of_week: Mapped[int] = mapped_column(Integer, index=True)  # 0 = Saturday, 6 = Friday
    start_time: Mapped[time] = mapped_column(Time, nullable=False)
    end_time: Mapped[time] = mapped_column(Time, nullable=False)

    # Relationships
    company = relationship("Company")
